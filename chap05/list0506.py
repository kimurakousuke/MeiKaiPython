# 用于算数运算的内置函数的应用实例

x = float(input('实数x：')) 
y = float(input('实数y：')) 
z = float(input('实数z：')) 

print('abs(x) = ', abs(x)) 
print('bool(x) = ', bool(x)) 
print('divmod(x, y) = ', divmod(x, y)) 
print('max(x, y) = ', max(x, y)) 
print('min(x, y) = ', min(x, y)) 
print('pow(x, y) = ', pow(x, y)) 
print('round(x, 2) = ', round(x, 2)) 
print('round(x, 3) = ', round(x, 3)) 
print('sum(x, y, z) = ', sum((x, y, z)))