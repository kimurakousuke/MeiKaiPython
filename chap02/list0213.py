# 读取两个整数进行加减乘除（其三：使用str函数精炼代码并输出） 

a = int(input('整数a：'))
b = int(input('整数b：'))

print('a + b 等于'+ str( a + b)+ '。') 
print('a - b 等于' + str(a - b) + '。') 
print('a * b 等于' + str(a * b) + '。') 
print('a / b 等于'+str( a / b)+ '。') 
print('a // b 等于'+str( a // b)+ '。') 
print('a % b 等于'+str( a % b)+ '。') 
print('a ** b 等于'+str( a ** b)+ '。')