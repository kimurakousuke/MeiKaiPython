# 读取两个整数值并进行加减乘除（其四：使用format精炼代码并输出） 

a = int(input('整数a：')) 
b = int(input('整数b：')) 

print('a + b 等于{}。'.format(a + b)) 
print('a - b 等于{}。'.format(a - b)) 
print('a * b 等于{}。'.format(a * b)) 
print('a / b 等于{}。'.format(a / b)) 
print('a // b 等于{}。'.format(a // b)) 
print('a % b 等于{}。'.format(a % b)) 
print('a ** b 等于{}。'.format(a ** b))
