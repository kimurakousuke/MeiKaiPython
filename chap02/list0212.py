# 读取两个整数并进行加减乘除（其二：读取和转换合并，以单一语句实现） 

a = int(input('整数a：')) 
b = int(input('整数b：'))

print('a + b 等于', a + b, '。') 
print('a - b 等于', a - b, '。') 
print('a * b 等于', a * b, '。') 
print('a / b 等于', a / b, '。') 
print('a // b 等于', a // b, '。') 
print('a % b 等于', a % b, '。') 
print('a ** b 等于', a ** b, '。')
