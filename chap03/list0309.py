# 打印输出逻辑型的值 

a = int(input('整数a：')) 
b = int(input('整数b：')) 

print('a < b ：', a < b) 
print('a <= b ：', a <= b) 
print('a > b ：', a > b) 
print('a >= b ：', a >= b) 
print('a == b ：', a == b) 
print('a != b ：', a != b) 

print('False ：', False) 
print('True ：', True) 
print('True + 5：', True + 5) # 被程序视作1 + 5
