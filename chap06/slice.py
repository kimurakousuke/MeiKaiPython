# 分片表达式的应用实例

s = 'ABCDEFGHIJKLMNOPQRSTUVWZYZ'
print('s[:]    = {}'.format(s[:]))
print('s[:7]   = {}'.format(s[:7]))
print('s[5:]   = {}'.format(s[5:]))
print('s[5:11] = {}'.format(s[5:11]))
print('s[-5:]  = {}'.format(s[-5:]))
print('s[::3]  = {}'.format(s[::3]))
print('s[::-1] = {}'.format(s[::-1]))
